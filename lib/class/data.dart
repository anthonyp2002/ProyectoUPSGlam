class DatosContenido {
  final String imgUrl;       
  final String fecha;       
  final String descripcion; 
  final List<String> comentarios; 
  final int likes;           
  final String name;        
  final String profileImgUrl; 

  DatosContenido({
    required this.imgUrl,
    required this.fecha,
    required this.descripcion,
    required this.comentarios,
    required this.likes,
    required this.name,
    required this.profileImgUrl,
  });

}
